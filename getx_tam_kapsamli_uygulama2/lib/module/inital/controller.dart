///  `onInit()`, `onClose()`, `onDispose()` gibi GetX lifecycle metotlarını
///  kullanabilir.  İleride,  `InitalPage` ekranının durumunu yönetmek, veri
///  almak veya işlemler gerçekleştirmek için bu controller'a metotlar ve
///  değişkenler eklenebilir.
